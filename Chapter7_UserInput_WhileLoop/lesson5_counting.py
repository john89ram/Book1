# Intro to while loops - While loops will continue to run until a condition is true
    # Lets dip our toes into the this function by creating a program to run until it counts to a certain number

current_num = 1
while current_num <= 5:
    print(current_num)
    current_num += 1

# Here we establish a while condition to run until the "current_num" is 5
    # The next line will inform us what the current number is
    # The last line will add 1 to the current number and it will start over again
    # Once the current_num = 5, the program will stop.